class MenuItem {
  String? dish;
  int? rate;

  MenuItem({this.dish, this.rate});

  MenuItem.fromJson(Map<String, dynamic> json) {
    dish = json['dish'];
    rate = json['rate'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = Map<String, dynamic>();
    data['dish'] = dish;
    data['rate'] = rate;
    return data;
  }
}

class MenuList {
  List<MenuItem?>? items;

  MenuList({this.items});

  MenuList.fromJson(Map<String, dynamic> json) {
    if (json['items'] != null) {
      items = <MenuItem>[];
      json['items'].forEach((v) {
        items!.add(MenuItem.fromJson(v));
      });
    }
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = Map<String, dynamic>();
    data['items'] =
        items != null ? items!.map((v) => v?.toJson()).toList() : null;
    return data;
  }
}
